                </p><p><strong><span style="color: red; font-size: 16px">В 1941 году</span></strong> на базе школы в течении нескольких месяцев работала «Школа молодого бойца» по подготовке добровольцев перед отправкой на фронт.
                </p><p><strong><span style="color: red; font-size: 16px">В 1942 году </span></strong>директор школы <strong>Студенцов А.И. </strong>вместе со своими учениками ушел на фронт.
