            Вся внутришкольная организационно-педагогическая работа гимназии направлена на совершенствование деятельности учителей и учащихся, повышение делового мастерства и творчества педагогов, повышению качества знаний и уровня воспитанности учащихся.</br>
            Педколлектив постоянно направляет свою работу на развитие учащихся, их творческого и интеллектуального потенциала:</br></p>
           <p>Внедряет программы углубленного изучения предметов: математики, английского языка, истории, биологии, географии;</p>
            <p>Программы профильных курсов: химико-биологический, экономико-правовой, физико-математический, языковой;</p>
            <p>Вводит предметы дополнительного образования: экономика, граждановедение, валеология, ритмика, экология, правовая этика, риторика.</p>

